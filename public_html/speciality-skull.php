<h6> ANTERIOR SKULL BASE SURGERY - ENDOSCOPE ASSITED -(by Dr. Sanjay Sachdeva) </h6>
<p> Skull base is one of the most varied anatomical challenges of the human body. Approach is fraught with problems of access to important physiological structures. Endoscopes with cold light system and cameras have improved access and visualization, thus brightening up the field of surgical interest enabling one to see and approach every niche and corner of this intricate and complex area. Endoscopes can reach areas directly without disturbing anatomy, which conventional surgery would need to go through important structures to achieve the same results. Thus, it is minimally invasive, reducing the morbidity and performing precisely and  effectively. </p>
<p> Aftercare becomes easier, reducing the period of hospitalization as well. </p>
<p> The list of indications with this approach appears to be increasing as more and more people are using the endoscopic way. </p>


