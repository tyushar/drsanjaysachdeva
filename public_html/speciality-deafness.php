<h6> Conductive hearing loss </h6>
<p> Conductive hearing loss is caused by any condition or disease that impedes the conveyance of sound in its mechanical form through the middle ear cavity to the inner ear. A conductive hearing loss can be the result of a blockage in the external ear canal or can be caused by any disorder that unfavorably effects the middle ear's ability to transmit the mechanical energy to the stapes footplate. This results in reduction of one of the physical attributes of sound called intensity (loudness), so the energy reaching the inner ear is lower or less intense than that in the original stimulus. Therefore, more energy is needed for the individual with a conductive hearing loss to hear sound, but once it's loud enough and the mechanical impediment is overcome, that ear works in a normal way. Generally, the cause of conductive hearing loss can be identified and treated resulting in a complete or partial improvement in hearing. </p>

<h6> Sensorineural Hearing Loss </h6>
<p> The second type of hearing loss is called sensorineural hearing loss. This word can be divided into its two components - sensory and neural - to allow us more clarity in specifying the type of hearing loss. The comprehensive audiometric assessment and supplemental tests can yield the information needed to differentiate between a sensory and a neural hearing loss, although they can co-exist in the same ear. Neural hearing loss is another name for retrocochlear hearing loss. </p>
<p> Sensorineural hearing loss results from inner ear or auditory nerve dysfunction. The sensory component may be from damage to the organ of Corti or an inability of the hair cells to stimulate the nerves of hearing or a metabolic problem in the fluids of the inner ear. The neural or retrocochlear component can be the result of severe damage to the organ of Corti that causes the nerves of hearing to degenerate or it can be an inability of the hearing nerves themselves to convey neurochemical information through the central auditory pathways. </p>
<p> The reason for sensorineural hearing loss sometimes cannot be determined, it does not typically respond favorably to medical treatment, and it is typically described as an irreversible, permanent condition. Like conductive hearing loss, sensorineural hearing loss reduces the intensity of sound, but it might also introduce an element of distortion into what is heard resulting in sounds being unclear even when they are loud enough. </p>

<h6> Mixed Hearing Loss </h6>
<p> A mixed hearing loss can be thought of as a sensorineural hearing loss with a conductive component overlaying all or part of the audiometric range tested. So, in addition to some irreversible hearing loss caused by an inner ear or auditory nerve disorder, there is also a dysfunction of the middle ear mechanism that makes the hearing worse than the sensorineural loss alone. The conductive component may be amenable to medical treatment and reversal of the associated hearing loss, but the sensorineural component will most likely be permanent. </p>

<h6> Treatment </h6>
<p> People with hearing loss delay hearing impairment treatment because they are unaware of the fact that receiving early hearing treatment has the potential to literally transform their lives. </p>
