<h6> Hearing Aids </h6>
<p> Sensorineural hearing loss or deafness tends to be permanent because it involves damage to nerves or to the inner ear hair cells. The only method of treatment is a hearing aid worn in the ear, a device that amplifies the volume of sound electronically. Now a days, most of the hearing aids are programmable to make them more effective for use in a variety of situations, such as noisy environments or talking on the phone. </p>

<p> The basic function of a hearing aid is as follows: </p>
<ul style="list-style-type:none; padding-left:0;">
<li> Sound waves enter through the microphone, which converts acoustic signals into electrical signals. </li>
<li> The amplifier increases the strength of the electrical signal. </li>
<li> From the amplifier, the signal is then transformed back to an acoustic signal by the receiver (a miniature loud speaker). </li>
<li> From the receiver the signal is channeled intro the ear canal, either through a small tube or through an ear mold. </li>
<li> A battery is required to power the hearing aid and enable the amplification process. </li>
