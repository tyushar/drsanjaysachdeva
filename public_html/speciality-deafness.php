<p> Hearing loss is the reduced ability to hear. Deafness is the complete inability to hear. </p>

<h6> Diagnosis </h6>
<p> A comprehensive audiologic evaluation must be completed in order to determine the types and severity of hearing loss to make appropriate recommendations for each patient. Pure tone and speech audiometry as well as the Immittance test battery must be completed, in addition to any additional assessments like BERA, ASSR , OAE necessary for an exhaustive profile of the hearing system. A balance test called electronystagmography (ENG) might also be needed if dizziness or imbalance is also a complaint. </p>

<h6> Types of Hearing Loss </h6>
<p> In general terms, there are two types of hearing loss, conductive and sensorineural. A combination of both is also seen as a mixed hearing loss. Each is discussed below. </p>					
