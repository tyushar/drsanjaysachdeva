<h6> ENDOSCOE ASSISTED EAR SURGERY (STITCHLESS SURGERY) </h6>

<h6> What is endoscopic ear surgery? </h6>
<p> Endoscopy of the ear is done by placing a rigid endoscope with camera into the ear to examine both the exterior and middle portions of the ear. The ear canal, ear drum(tympanic membrane) and middle ear can all be evaluated in the outpatient to understand the cause of the disease. </p>

<div class="pi-img-w pi-img-round-corners pi-img-shadow pi-img-right pi-margin-top-5" style="width: 350px;">
<img src="<?php echo base_url(); ?>img/endoscope-endoscope.jpg" alt=""/>
<span class="pi-img-caption"> Endoscope </span>
</div>

<p> The panoramic and brilliant view shows all the details of this intricate structure on a big screen. This helps a clinician in diagnosing and treating the ear diseases with greater precision. </p>
					
<h6> History </h6>
<p> Endoscopy for the middle ear was first described for Meret. al. in 1967. Nomura, in 1982, was the first to pass an endoscope through a small opening in the ear drum and examine the middle ear cavity. Poe et al popularized endoscopy in otology in early 1990s. Things have evolved a great extent from there on. </p>

