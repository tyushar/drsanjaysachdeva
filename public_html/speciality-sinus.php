						Sinuses
					</h3>
					
					<p> Sinuses are air-containing cavities, which communicate with the nose through their natural openings. There are multiple functions attributed to the sinuses such as resonators for the voice, protection of the brain, serving the sense of smell and helps the nose in its various functions. </p>

					<div class="pi-img-w pi-img-round-corners pi-img-shadow pi-img-left pi-margin-top-5" style="width: 350px;">
