<p> In any case sinuses are a major cause of illness to many people. </p>
<h6> Various Symptoms of Nose and Sinuses: </h6>
<ul style="list-style-type:none; padding-left:0;">
<li> <i style="font-size:19px;" class="icon-dot"> </i> Nasal obstruction </li>
<li> <i style="font-size:19px;" class="icon-dot"> </i> Stuffiness </li> 
<li> <i style="font-size:19px;" class="icon-dot"> </i> Congestion </li>
<li> <i style="font-size:19px;" class="icon-dot"> </i> Inability to blow the nose </li>
</ul>
<p> This may vary from a simple common cold occurring occasionally to a constant feeling of blocked nose that many people suffer due to various conditions. The common causes of nasal obstruction are as follows: </p>

<h6> a) Deviation of the nasal septum – </h6>
<p> Septum is a structure inside the nose, which separates the nasal cavity into right and left side. Septum is important as it allows orderly distribution of airflow throughout the nasal cavity in a uniform fashion. If for example the nasal septum is absent or is perforated as a result of a previous operation, disease, irritation, or injury; the air we breathe in becomes turbulent and airflow is not evenly distributed. In this situation in spite of a large nasal cavity volume, the patient still complains of nasal obstruction. 
In a significant number of people especially in men, deviated nasal septum is due a history of nasal injury. This may be quite severe or may be trivial in nature. The treatment of deviated nasal septum causing nasal obstruction is surgical correction of the septum either under local anesthetic or general anesthetic. However, it is important to remember that deviated nasal septum is a common occurrence in the community at large and does not require surgical treatment just because it is deviated, unless it is causing blockage of airway. The specialist has to assess that the deviated septum is actually responsible for the patient’s symptoms and there is no other medical cause for nasal blockage. The specialist must be convinced that the symptoms shown by the patient are correction warranted and would help the patient. </p>
<h6> b) Rhinitis – </h6>
<p> The term rhinitis simply means inflammation of the lining of the nose. The inflammation of the nasal lining need not be due to bacteria or viruses. Drugs, chemicals, paints or allergens in the surrounding environment, either at work or at home, may initiate rhinitis. There are several types of rhinitis. Some of the common ones are as follows: </p> 
<ul style="list-style-type:none;">
<li> <strong> 1) Allergic Rhinitis – </strong> In this condition the person suffers from allergy to various substances. There are many causes, but the most common are, allergy to house dust and the house dust mite.  a microscopic organism found in damp, dark corners inside the house. The human being is sensitive to the shedding of the skin of this organism. The person could also be suffering from what is commonly known as hay fever, which is allergy to grass, flower and various tree pollens in the hay fever season i.e., the summer months of the year. This is sometimes associated with asthma in some people. 
The best management of allergic rhinitis obviously is to avoid the substance or allergen, which usually is not so easy. In the acute stage depending upon the severity, treatment with nasal steroid sprays, antihistamines, decongestants and bed rest is usually effective. 
The long-term effectiveness of treatment at times requires allergy testing and immunotherapy which actually means boosting up the immune system against the allergen. </li>
</ul>

<h6> c) Nasal polyps </h6>
<p> Nasal polyps are nothing but simply a thick skin of the nose and sinuses, which have grown into the nasal cavity. This leads to feeling of nasal obstruction or a person suffering from constant colds. </p>
<p> This symptom may be associated with other allergic complains of excessive sneezing, runny nose, itching in the nose etc. </p>
<p> It is very important to note that nasal polyp is just a symptom of some other underlying condition. It is NOT a diagnosis, which means the specialist has to work out the causes of nasal polyps in an individual patient and treatment then follows accordingly. The unusual causes of polyps are certain benign and malignant tumors affecting nose and sinuses which are usually on one side of the nose. Very rarely in young babies what appears to be polyp may be part of the brain or its coverings hanging into the nose. </p>
<p> The most common causes of polyps are: </p>
<ul style="list-style-type:none; padding-left:0;">
<li> 1) Chronic inflammation of the nose and sinuses </li>
<li> 2) Nasal Allergy </li>
<li> 3) Polyps due to fungus inside the nose and sinuses </li>
</ul>
<p> These common polyps are usually on both sides of the nose and are of long standing duration. Another particular common polyp, which arises from the large sinus called maxillary sinus, usually occurs in adolescents and young adults. This occurs on one side of the nose and causes nasal blockage. </p>
<p> If a polyp occurs in adolescents and young male adults, who give a history of nosebleeds, which may be quite severe at times. This may be due to a benign tumor which bleeds profusely and needs immediate medical attention. This tumor can invade the bone and extend towards the brain. These patients need special investigations and removal of the tumor by an experienced surgeon. </p>

<h6> Sinusitis </h6>
<p> The symptoms of sinusitis are often difficult to distinguish from colds or allergies. Many people do not need to see their doctor in spite of the fact that colds have persisted. Instead, they try to treat themselves with over-the-counter medications. However, unlike a cold or allergy, bacterial sinusitis requires a proper diagnosis and treatment with an antibiotic. </p>

<p> The sinusitis is caused by 1) bacteria 2) viruses 3) allergies 4) Fungus  </p>
<p> The following are symptoms of sinusitis: </p>
<ul style="list-style-type:none; padding-left:0;">
<li> 1) headaches, </li>
<li> 2) facial pain (pain around the eyes, upper teeth etc.) </li>
<li> 3) nasal stuffiness, </li>
<li> 4) yellow-green nasal discharge, </li>
<li> 5) low grade fever or bad breath </li>
<li> 6) post nasal drip. </li>
</ul>

<p> Headaches which are persistent and are associated with fits or other sinister symptoms need expert opinion. </h6>
<p> If the sinus infection lasts for three months or more it can become chronic. The treatment is aimed at eradicating the infection with the help of antibiotics. decongestants help improve the drainage and proper ventilation of the sinuses. It is advisable to use drugs that thin out the thick mucus and allow it drain freely through the openings of the sinuses. Plenty of fluids are recommended for the same reason. </p>
<p> In case the sinus infection does not settle down, it is advisable to see a specialist. Surgery is sometimes necessary to remove obstruction which is hampering free drainage and ventilation of the sinuses. </p>

