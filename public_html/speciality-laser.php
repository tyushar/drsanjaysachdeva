					<p> Before LASIK eye surgery, you will meet with a coordinator or eye surgeon who will discuss what to expect during and after the procedure. During this session, your medical history will be evaluated and your eyes will be fully examined. Likely initial tests include measuring corneal thickness, refraction, corneal mapping, air pressure, and pupil dilation. Once you have gone through the initial evaluation, you will meet the surgeon, who will answer any questions you may have. Afterwards, you can schedule an appointment for the procedure.During LASIK eye surgery, an instrument called a microkeratome or femtosecond laser is used to create a thin flap in the cornea.  </p>
					<p> The cornea flap is then painlessly peeled back and the underlying corneal tissue is reshaped using another laser. After the cornea is reshaped so that it can properly focus light onto the retina, the cornea flap is put back in place and the surgery is complete.Refractive surgery is the term used to describe surgical procedures that correct common vision problems (nearsightedness, farsightedness, astigmatism and presbyopia) to reduce your dependence on prescription eyeglasses and/or contact lenses.Currently, a laser procedure called LASIK (LAY-sik) is the most popular refractive surgery performed in the United States.  </p>
					<p> But there are other types of refractive surgery — including other laser procedures and intraocular lens procedures — that might be an even better choice for you, depending on your needs.When the LASIK procedure is finished, your surgeon will reposition the corneal flap on your eye and rinse it with liquid. You may feel the urge to blink from the liquid but the eyelid holder will keep you from blinking.  Your surgeon will them let the flap reattach to the surface of your eye. This will take about 30-60 seconds. During this time the surgeon and technician will remove the eyelid holder and any tape from your eyes. Your surgeon will then ask you to blink a few times.  </p>
					<p> The surgeon will now repeat the procedure on your other eye. The eye that was just treated will have a patched placed over it.The treatment day process will differ dependent upon the type of treatment that you are having. The clinic team will take you through the treatment day process for your individual treatment plan at your consultation.Regardless of treatment type, most people are nervous on their treatment day. </p>
