<h6> Self Help </h6>
<ul>
<li> Sleep on your side </li>
<li> Elevate the head of your bed </li>
<li> Limit alcohol and sleep medication </li>
<li> Exercise regularly </li>
<li> Lose weight </li>
</ul>

<h6> Serious Implications </h6>
<p> Severe OSAS can lead to a potentially life threatening condition requiring immediate medical attention. </p>
<p> The risks involved are: </p>
<ul>
<li> Excessive day time sleepiness - can result in accidents </li>
<li> Heart attacks (studies show that OSAS patients have 24 times higher risk of having heart attacks in sleep) </li>
<li> Strokes (paralysis, etc.) </li>
<li> Irregular heart beat </li>
<li> High Blood Pressure </li>
<li> Fortunately, all of this is treatable. </li>
</ul>

<h6> Treatment </h6>
<p> Snoring can often seem like a problem without a solution especially when it is getting in the way of a good night's sleep for you and your partner. If you want to stop snoring, it is important to consider the cause and severity of this ailment. Identifying the cause can help you choose the right treatment. </p>
<p> Until now two modalities are known to treat this condition: </p>
<ul style="list-style-type:none; padding-left:0;">
<li> <i style="font-size:19px;" class="icon-dot"> </i> One is using a machine in the night known as CPAP,which gives pressure of air to keep the airway open. </li>
<li> <i style="font-size:19px;" class="icon-dot"> </i> Other is surgical correction of obstructing areas. </li>
</ul>
<p> In 1990s, surgical laser treatment came on line to give relief for snorers.This came to be known as Laser Assisted Uvulopalatoplasty. It is possible to do the procedure as an outpatient treatment. Post treatment patients are prescribed medicines for 10 to14 days. If areas of obstruction are large, two or three sittings may be required every three to four weeks. Patient is allowed to eat soft diet on the same day. Bleeding with lasers is very minimal. Most of the time we combine other energy producing source like
radiofrequency to have better outcomes for snorers and obstructive sleep apnea. This appears to be an encouraging alternative to sleeping daily with CPAP machine, especially people who are frequent travellers. </p>
<p> Details of treatment are individualized and are best discussed with your specialist doctor. </p>


