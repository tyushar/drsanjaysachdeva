					<p> When the world goes to bed at night, some experience a good night sleep while others snore with discomfort to others. This is not only a social problem but can lead to serious medical issues. </p>

<h6> What is Snoring? </h6>
<p> Air passes through the passage of nose, throat and back of tongue to enter the windpipe and the lungs. This is vital for oxygen supply to our body. When any of these passages are narrow, adjoining soft tissues of throat or tongue collapse. Collapsible soft tissues start vibrating with passage of air and produce unpleasant sounds of snoring. </p>

					<div class="pi-img-w pi-img-round-corners pi-img-shadow pi-img-left pi-margin-top-5" style="width: 350px;">
						<img src="<?php echo base_url(); ?>img/snoring-page-one.jpg" alt=""/>

					</div>

					
					
<h6> What is Sleep Apnea? </h6>
<p> In the night, when muscles relax, the airway collapse can be severe to obstruct air entering the lungs. This condition can lead to futile attempts in breathing with oxygen supply drastically. This period of no breathing is called Apnea. Reduction in oxygen affects the body overall. This can lead to a medical condition known as OBSTRUCTIVE SLEEP APNEA SYNDROME (OSAS). </p>


<h6> Common Symptoms </h6>
<ul>
<li> Loud snoring during sleep </li>
<li> Excessive day time sleepiness </li>
<li> Does not feel fresh on awakening </li>
<li> Decrease concentration during the day </li>
<li> Attacks of choking in the night (patient may not be aware and may be noticed by partner) </li>
<li> Reduced memory </li>
<li> Irritability </li>
<li> Depression, personality changes </li>
<li> Reduces libido </li>
<li> Children may be hyperactive with reduced attention span </li>
<li> Chest pulls in during sleep (especially in children) </li>
</ul>

